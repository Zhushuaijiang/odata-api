using System;
using System.Collections.Generic;
using System.Linq;
using System.Linq.Expressions;
using System.Text;
using System.Threading.Tasks;
using Yoisoft.DataBase.Repository;
using Yoisoft.Util;

namespace Yoisoft.Application.Base
{

    public class CODE_POSTService : RepositoryFactory
    {
        #region 属性 构造函数
        private string fieldSql;
        public CODE_POSTService()
        {
            fieldSql = @" t.POSTID,
                          t.POSTNAME
                        ";
        }
        #endregion
        #region 数据 查询

        public IEnumerable<CODE_POSTEntity> RecordPagination(Pagination pagination)
        {
            try
            {
                var strSql = new StringBuilder();
                strSql.Append("SELECT ");
                strSql.Append(fieldSql);
                strSql.Append(" FROM YY_CODE_POST t ");
                return this.BaseRepository().FindList<CODE_POSTEntity>(strSql.ToString(), pagination);
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }

        public IEnumerable<CODE_POSTEntity> RecordQuery()
        {
            try
            {
                var strSql = new StringBuilder();
                strSql.Append("SELECT ");
                strSql.Append(fieldSql);
                strSql.Append(" FROM YY_CODE_POST  t");
                return this.BaseRepository().FindList<CODE_POSTEntity>(strSql.ToString());
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }
        public IQueryable<CODE_POSTEntity> IQueryRecord(Expression<Func<CODE_POSTEntity, bool>> condition)
        {
            try
            {
                return this.BaseRepository().IQueryable(condition);
            }
            catch (Exception ex)
            {

                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }

        }



        public CODE_POSTEntity GetEntity(string keyValue)
        {
            try
            {
                int id = 0;
                int.TryParse(keyValue, out id);
                return this.BaseRepository().FindEntity<CODE_POSTEntity>(t => t.POSTID == id);
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }


        #endregion

        #region 操作数据
        public void PhysicalDelRecord(string keyValue)
        {
            try
            {
                CODE_POSTEntity entity = new CODE_POSTEntity()
                {
                    POSTID = Convert.ToInt32(keyValue)
                };
                this.BaseRepository().Delete(entity);
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }
