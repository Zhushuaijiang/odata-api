

using System;
using System.Collections.Generic;
using System.Linq;
using System.Linq.Expressions;
using System.Text;
using System.Threading.Tasks;
using Yoisoft.DataBase.Repository;
using Yoisoft.Util;

namespace Yoisoft.Application.Base
{

    public class CODE_ITEMService : RepositoryFactory
    {
        #region 属性 构造函数
        private string fieldSql;
        public CODE_ITEMService()
        {
            fieldSql = @" t.ID,
                          t.ITEMID,
                          t.ITEMNAME,
                          t.ORDERINDEX
                        ";
        }
        #endregion
        #region 数据 查询

        public IEnumerable<CODE_ITEMEntity> RecordPagination(Pagination pagination)
        {
            try
            {
                var strSql = new StringBuilder();
                strSql.Append("SELECT ");
                strSql.Append(fieldSql);
                strSql.Append(" FROM YY_CODE_ITEM t ");
                return this.BaseRepository().FindList<CODE_ITEMEntity>(strSql.ToString(), pagination);
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }

        public IEnumerable<CODE_ITEMEntity> RecordQuery()
        {
            try
            {
                var strSql = new StringBuilder();
                strSql.Append("SELECT ");
                strSql.Append(fieldSql);
                strSql.Append(" FROM YY_CODE_ITEM  t");
                return this.BaseRepository().FindList<CODE_ITEMEntity>(strSql.ToString());
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }
        public IQueryable<CODE_ITEMEntity> IQueryRecord(Expression<Func<CODE_ITEMEntity, bool>> condition)
        {
            try
            {
                return this.BaseRepository().IQueryable(condition);
            }
            catch (Exception ex)
            {

                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }

        }



        public CODE_ITEMEntity GetEntity(string keyValue)
        {
            try
            {
                int id = 0;
                int.TryParse(keyValue, out id);
                return this.BaseRepository().FindEntity<CODE_ITEMEntity>(t => t.ID == id);
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }


        #endregion

        #region 操作数据
        public void PhysicalDelRecord(string keyValue)
        {
            try
            {
                CODE_ITEMEntity entity = new CODE_ITEMEntity()
                {
                    ID = Convert.ToInt32(keyValue)
                };
                this.BaseRepository().Delete(entity);
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowServiceException(ex);
                }
            }
        }
